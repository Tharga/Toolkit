namespace Tharga.Toolkit
{
    /// <summary>
    /// Represents the time units available for duration formatting, ordered from largest to smallest.
    /// </summary>
    public enum EUnit
    {
        /// <summary>Year time unit.</summary>
        Year,
        /// <summary>Month time unit.</summary>
        Month,
        /// <summary>Week time unit.</summary>
        Week,
        /// <summary>Day time unit.</summary>
        Day,
        /// <summary>Hour time unit.</summary>
        Hour,
        /// <summary>Minute time unit.</summary>
        Minute,
        /// <summary>Second time unit.</summary>
        Second,
        /// <summary>Millisecond time unit.</summary>
        Millisecond
    }
}